public class AssingmentOperator {
    public static void main(String[] args) {
        int x = 3;
        x += 4;
        System.out.println("x = "+x);
        x -= 1;
        System.out.println("x = "+x);
        x *= 1;
        System.out.println("x = "+x);
        x /= 1;
        System.out.println("x = "+x);
        x %= 1;
        System.out.println("x = "+x);
    }
}